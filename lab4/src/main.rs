// Вычисляет модуль (длину) вектора
// Принимает срез массива из f64, возвращает f64
fn magnitude(vector: &[f64; 3]) -> f64 {
    // Извлекаем координаты вектора для удобства
    let x = vector[0]; // первая координата (индекс 0)
    let y = vector[1]; // вторая координата (индекс 1)
    let z = vector[2]; // третья координата (индекс 2)
    
    // Вычисляем сумму квадратов координат
    let sum_of_squares = x * x + y * y + z * z;
    
    // Возвращаем квадратный корень из суммы квадратов
    sum_of_squares.sqrt() // метод sqrt() вычисляет квадратный корень
}

// Нормализует вектор (делает его длину равной 1)
// Принимает изменяемую ссылку на массив и изменяет его
fn normalize(vector: &mut [f64; 3]) {
    // Вычисляем текущий модуль вектора, используя функцию magnitude
    let mag = magnitude(vector);
    
    // Делим каждую координату на модуль вектора
    vector[0] /= mag; // x-координата делится на модуль
    vector[1] /= mag; // y-координата делится на модуль
    vector[2] /= mag; // z-координата делится на модуль
}

// Используйте эту функцию main для проверки своей работы.
fn main() {
    // Проверяем модуль единичного вектора (должен быть 1.0)
    println!("Модуль единичного вектора: {}", magnitude(&[0.0, 1.0, 0.0]));

    let mut v = [3.0, 4.0, 9.0]; // создаём изменяемый вектор
    println!("Модуль {v:?}: {}", magnitude(&v)); // выводим исходный модуль
    normalize(&mut v); // нормализуем вектор
    println!("Модуль {v:?} после нормализации: {}", magnitude(&v)); // проверяем результат (должен быть 1.0)
}